    private boolean validatePaymentInput() {
        if("".equals(NameCard_text.getText())){
            ErrorDialog("Enter name on Card!!","Invalid Feilds" );
            return false;
        }
        if(NoCard_text.getText().equals("")){
            ErrorDialog("Enter card number!!", "Invalid Feilds");
            return false;
        }
        if(NoCard_text.getText().length()!=16){
            ErrorDialog("Enter valid card number!!", "Invalid Feilds");
            return false;
        }
        if("".equals(date1_text.getText())){
            ErrorDialog("Enter month of expiry!!", "Invalid Feilds");
            return false;
        }
        if(Integer.valueOf(date1_text.getText())<1 || Integer.valueOf(date1_text.getText())>12 ){
            ErrorDialog("Enter valid month!!", "Invalid Feilds");
            return false;
        }
        if("".equals(date2_text.getText())){
            ErrorDialog("Enter year of expiry!!", "Invalid Feilds");
            return false;
        }
        if(Integer.valueOf(date2_text.getText())<2014 ){
            ErrorDialog("Enter valid year!!", "Invalid Feilds");
            return false;
        }
        if( "".equals(cvv_text.getText())){
            ErrorDialog("Enter CVV!!", "Invalid Feilds");
            return false;
        }
        if (cvv_text.getText().length()!=3){
            ErrorDialog("Enter valid CVV!!", "Invalid Feilds");
            return false;
        }
        return true;
    }

